przyczynił się do        znaczącego rozwoju sił i środków podległej mu JW 4101. 5    września 2012 r. z powodu wyjazdu zagranicznego na studia    płk Pietras przekazał dowodzenie dotychczasowemu Zastępcy    D-cy ppłk. Sławomirowi Drumowiczowi. Pułkownik Pietras      podjął studia podyplomowe polityki obronnej w Royal          College of Defence Studies w Londynie.
