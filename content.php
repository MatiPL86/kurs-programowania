<p>
Jednostka Wojskowa 4101 jest najstarszą jednostką wojskową polskich komandosów. Została sformowana w 1961 roku.
</p>

<p>
Poprzednia, znacząca zmiana etatu, skutkująca zmianą JW      4101 z batalionu na pułk była efektem Zarządzenia Szefa      SG WP z dnia 8 października 1993 r. JW 4101 stała się 1      Pułkiem Specjalnym. Zarządzenie przeformowało 1              Samodzielny Batalion Szturmowy. Wyróżnik Komandosów pułk    otrzymał w 1995 r. Pułk w latach 1993-2007 podlegał          rozkazom Dowódcy Wojsk Lądowych i był w owym czasie          jedyną samodzielną jednostką specjalną SZ RP (poza Gromem    okresu 1999-2007).
</p>

<p>
W 2010 roku po raz pierwszy wyłoniono Dowódcę JW 4101        przez konkurs[1]. Najlepszy okazał się ówcześnie Zastępca    Dowódcy JW 4101 ppłk Ryszard Pietras[2]. Dowódca Pietras    choć wywodzi się z wojsk chemicznych (dowodził 5            Tarnogórskim Batalionem Chemicznym)
</p>
    
