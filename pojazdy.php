    Niemiecki pojazd opancerzony powstał na bazie Mercedesa Unimoga. Opinia niezwykle wytrzymałej konstrukcji nie wzięła się znikąd. Dingo 2 składa się z opancerzonej kapsuły, która dodatkowo jest otoczona przez nadwozie.

<img class="srodek" src="pojazd%20woj%20dingo.jpg" title="DINGO"/>

    Warstwowa struktura sprawia, że w trakcie testów pojazd wytrzymał wybuch 7-kilogramowej miny lądowej. Żaden z 8 pasażerów nie odniósł obrażeń. Ponadto 218-konny silnik wysokoprężny rozpędza Dingo do 90 km/h i zapewnia maksymalny zasięg na poziomie 1000 kilometrów.

